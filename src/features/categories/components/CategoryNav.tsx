import { useState } from 'react'
import type { CategorySimple } from '../types/category'

interface CategoryNavProps {
  categories: CategorySimple[]
  onCategoryChange?: (categoryLabel: string) => void
}

export function CategoryNav({
  categories,
  onCategoryChange
}: CategoryNavProps) {
  const [activeCategory, setActiveCategory] = useState(
    categories[0]?.label || null
  )

  const handleCategoryClick = (categoryLabel: string) => {
    setActiveCategory(categoryLabel)
    if (onCategoryChange) {
      onCategoryChange(categoryLabel)
    }
  }

  const getIcon = (iconName: string) => {
    const Icon =
      {
        mobile: MobileIcon,
        laptop: LaptopIcon,
        tablet: TabletIcon,
        audio: AudioIcon,
        wearable: WearableIcon,
        camera: CameraIcon,
        gaming: GamingIcon,
        network: NetworkIcon,
        accessories: AccessoriesIcon
      }[iconName] || MobileIcon
    return <Icon />
  }

  return (
    <div className="w-full bg-white py-8">
      <div className="max-w-[1440px] mx-auto px-4 md:px-[50px]">
        <div className="flex items-center justify-center gap-16 flex-wrap">
          {categories.map(category => {
            const isActive = activeCategory === category.label
            return (
              <button
                key={category.id}
                onClick={() => handleCategoryClick(category.label)}
                className={`flex flex-col items-center justify-center gap-2 p-2 ${
                  isActive ? 'border-b-[2px] border-primary' : ''
                }`}
              >
                <div className="w-8 h-8 text-[#444444] flex items-center justify-center">
                  {getIcon(category.icon)}
                </div>
                <span className="text-l font-light text-[#444444]">
                  {category.label}
                </span>
              </button>
            )
          })}
        </div>
      </div>
    </div>
  )
}

